local CATEGORY_NAME = "custom shit"

function ulx.break(calling_ply, target_ply)
	target_ply:SendLua("LocalPlayer = nil")
	ulx.fanceLogAdmin(calling_ply, false, "#A broke #T", target_ply)
end

local c = ulx.command(CATEGORY_NAME, "ulx break", ulx.break, "!break", true)
c:addParam{ type=ULib.cmds.PlayerArg }
c:defaultAccess(ULib.ACCESS_ADMIN)
c:help("Breaks and player")


