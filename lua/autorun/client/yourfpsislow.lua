timer.Simple(10, function()

chat.AddText("Loading LOW FPS detector")
print ("frames: " .. math.Round( 1 / FrameTime()) .. "")

timer.Create("fpschecker", 5, 0, function()
myrealfps = math.Round( 1 / FrameTime())

if myrealfps < 10 then
chat.AddText("Hey, your frames is pretty low, which is below 10")
chat.AddText("You should probably type /fps in chat for some smoother experience")
surface.PlaySound("ambient/alarms/warningbell1.wav")
timer.Remove("fpschecker")
end

end)

chat.AddText("Loaded")

end)

