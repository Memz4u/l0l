hook.Add( "PlayerSay", "stfu", function( ply, text )
    local heckStart, heckEnd = string.find( text:lower(), "xd" )
    if heckStart then
        local ddd = RunConsoleCommand("ulx", "explode", "shadow")
        return ddd
 
    end
end )

